/* K.Kafara */

#include <stdlib.h>
#include <stdio.h>


int main(void)
{
    static int dx[3] = {-1, 0, -1};
    static int dy[3] = {0, -1, -1};

    int n, x0, y0, xk, yk;

    ////////////////////////////////////////////////////////////////////
    /// Wejście
    ////////////////////////////////////////////////////////////////////
    scanf("%d", &n);

    int board[n][n];

    scanf("%d %d", &x0, &y0);
    scanf("%d %d", &xk, &yk);

    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j)
            scanf("%d", &board[i][j]);

/*     for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < n; ++j)
            printf("%d ", board[i][j]);
        printf("\n");
    }
 */
    ////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////
    /// Obliczamy wartości brzegowe
    ////////////////////////////////////////////////////////////////////
    int val = 1;
    for (int i = x0 + 1; i <= xk; ++i)
    {
        if (board[i][y0] == 0)
            val = 0;

        board[i][y0] = val;
    }

    val = 1;

    for (int i = y0 + 1; i <= yk; ++i)
    {
        if (board[x0][i] == 0)
            val = 0;

        board[x0][i] = val;
    }
    ////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////
    /// Obliczamy dynamicznie, wiersz po wierszu wartości w tablicy.
    ////////////////////////////////////////////////////////////////////
    for (int i = x0 + 1; i <= xk; ++i)
    {
        for (int j = y0 + 1; j <= yk; ++j)
        {
            if (board[i][j] != 0)
            {
                --board[i][j]; 
                for (int k = 0; k < 3; ++k)
                {
                    if (board[i + dx[k]][j + dy[k]] != 0)
                        board[i][j] += board[i + dx[k]][j + dy[k]];
                }

            }
        }
    }
    ////////////////////////////////////////////////////////////////////

/*     for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < n; ++j)
            printf("%d ", board[i][j]);
        printf("\n");
    } */
    ////////////////////////////////////////////////////////////////////
    /// Wynik
    ////////////////////////////////////////////////////////////////////
    printf("%d\n", board[xk][yk]);
    ////////////////////////////////////////////////////////////////////
    return 0;
}
