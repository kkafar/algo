void timer_start(Timer * timer)
{
    timer->start_time = clock();
}
////////////////////////////////////////////////////////////////////////////////
void timer_stop(Timer * timer)
{
    timer->stop_time = clock();
    timer->passed_time = ((double)(timer->stop_time - timer->start_time)) / CLOCKS_PER_SEC;
}
