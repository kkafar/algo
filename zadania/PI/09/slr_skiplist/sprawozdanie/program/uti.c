int getlevel(const int max)
{
    return (rand() % max + 1);
}
////////////////////////////////////////////////////////////////////////////////
int * int_get_rand_arr(const int size, const int max)
{
    int * arr = (int *)(malloc(sizeof(int) * size));
    for (int * ptr = arr; ptr < arr + size; ++ptr)
        *ptr = rand() % max;
    return arr;
}
////////////////////////////////////////////////////////////////////////////////
void int_print_arr(int * arr, const int size)
{
    for (int * ptr = arr; ptr < arr + size; ++ptr)
        printf("%d ", *ptr);
    printf("\n");
}
